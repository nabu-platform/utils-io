package be.nabu.utils.io.api;

public class EncodingRuntimeException extends RuntimeException {

	private static final long serialVersionUID = -5019719181405442226L;

	public EncodingRuntimeException() {
		super();
	}

	public EncodingRuntimeException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public EncodingRuntimeException(String arg0) {
		super(arg0);
	}

	public EncodingRuntimeException(Throwable arg0) {
		super(arg0);
	}
	
}
